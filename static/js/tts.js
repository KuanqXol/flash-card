// Text-to-Speech (TTS) client library

async function playWord(word, speed = 'normal') {
    if (!word || word.trim() === '' || word === 'Loading...') return;
    
    // Normalize word parameter
    word = word.trim();
    
    // Find all matching TTS buttons on the page
    const buttons = document.querySelectorAll(`.btn-tts[data-word="${word.replace(/"/g, '\\"')}"]`);
    
    // Filter matching speed if attribute exists, otherwise target all matching the word
    let activeButtons = Array.from(buttons).filter(btn => {
        const btnSpeed = btn.getAttribute('data-speed') || 'normal';
        return btnSpeed === speed;
    });
    if (activeButtons.length === 0) {
        activeButtons = Array.from(buttons);
    }
    
    // Set loading state on targeted buttons
    activeButtons.forEach(btn => {
        btn.classList.add('loading');
        const icon = btn.querySelector('i');
        if (icon) {
            // Save original icon class to restore later
            if (!btn.dataset.originalIconClass) {
                btn.dataset.originalIconClass = icon.className;
            }
            icon.className = 'ph ph-circle-notch';
        }
    });
    
    const url = `/api/tts?word=${encodeURIComponent(word)}&speed=${speed}`;
    
    // Polling loop to check if audio is ready
    let attempts = 0;
    const maxAttempts = 15; // up to 4.5 seconds
    
    const restoreButtons = () => {
        activeButtons.forEach(btn => {
            btn.classList.remove('loading');
            btn.classList.remove('playing');
            const icon = btn.querySelector('i');
            if (icon && btn.dataset.originalIconClass) {
                icon.className = btn.dataset.originalIconClass;
            }
        });
    };
    
    while (attempts < maxAttempts) {
        try {
            const response = await fetch(url);
            if (response.status === 200) {
                // Audio is ready! Play it.
                const blob = await response.blob();
                const audioUrl = URL.createObjectURL(blob);
                const audio = new Audio(audioUrl);
                
                audio.addEventListener('play', () => {
                    activeButtons.forEach(btn => {
                        btn.classList.remove('loading');
                        btn.classList.add('playing');
                        const icon = btn.querySelector('i');
                        if (icon && btn.dataset.originalIconClass) {
                            icon.className = btn.dataset.originalIconClass;
                        }
                    });
                });
                
                const cleanup = () => {
                    restoreButtons();
                    URL.revokeObjectURL(audioUrl);
                };
                
                audio.addEventListener('ended', cleanup);
                audio.addEventListener('error', () => {
                    cleanup();
                    playFallback(word, activeButtons, speed);
                });
                
                try {
                    await audio.play();
                } catch (playErr) {
                    console.warn("Audio play rejected, falling back:", playErr);
                    cleanup();
                    playFallback(word, activeButtons, speed);
                }
                return;
            } else if (response.status === 202) {
                // Audio is still being generated by the backend, poll again in 300ms
                attempts++;
                await new Promise(resolve => setTimeout(resolve, 300));
            } else {
                throw new Error(`API returned status ${response.status}`);
            }
        } catch (err) {
            console.warn("TTS API fetch error, using Web Speech API fallback:", err);
            restoreButtons();
            playFallback(word, activeButtons, speed);
            return;
        }
    }
    
    // Timeout fallback
    console.warn("TTS API polling timeout, using Web Speech API fallback");
    restoreButtons();
    playFallback(word, activeButtons, speed);
}

function playFallback(word, buttons = null, speed = 'normal') {
    if (!('speechSynthesis' in window)) {
        console.warn("Web Speech API not supported.");
        if (buttons) {
            buttons.forEach(btn => {
                btn.classList.remove('loading');
                btn.classList.remove('playing');
                const icon = btn.querySelector('i');
                if (icon && btn.dataset.originalIconClass) {
                    icon.className = btn.dataset.originalIconClass;
                }
            });
        }
        return;
    }
    
    try {
        window.speechSynthesis.cancel();
        
        const utterance = new SpeechSynthesisUtterance(word);
        utterance.lang = 'en-US';
        utterance.rate = speed === 'slow' ? 0.7 : 1.0;
        
        // Try to select American English voice
        const voices = window.speechSynthesis.getVoices();
        const usVoice = voices.find(v => {
            const lang = v.lang.toLowerCase().replace('_', '-');
            return lang === 'en-us' || lang.startsWith('en-us');
        });
        if (usVoice) {
            utterance.voice = usVoice;
        }
        
        if (buttons) {
            utterance.onstart = () => {
                buttons.forEach(btn => {
                    btn.classList.remove('loading');
                    btn.classList.add('playing');
                    const icon = btn.querySelector('i');
                    if (icon && btn.dataset.originalIconClass) {
                        icon.className = btn.dataset.originalIconClass;
                    }
                });
            };
            
            const cleanup = () => {
                buttons.forEach(btn => {
                    btn.classList.remove('loading');
                    btn.classList.remove('playing');
                    const icon = btn.querySelector('i');
                    if (icon && btn.dataset.originalIconClass) {
                        icon.className = btn.dataset.originalIconClass;
                    }
                });
            };
            
            utterance.onend = cleanup;
            utterance.onerror = cleanup;
        }
        
        window.speechSynthesis.speak(utterance);
    } catch (fallbackErr) {
        console.warn("Web Speech API failed:", fallbackErr);
        if (buttons) {
            buttons.forEach(btn => {
                btn.classList.remove('loading');
                btn.classList.remove('playing');
                const icon = btn.querySelector('i');
                if (icon && btn.dataset.originalIconClass) {
                    icon.className = btn.dataset.originalIconClass;
                }
            });
        }
    }
}

// Global Keyboard Shortcut Listener for 'P'
document.addEventListener('keydown', function(e) {
    if (e.target.tagName === 'INPUT' || e.target.tagName === 'TEXTAREA') return;
    
    if (e.key.toLowerCase() === 'p') {
        // Try to fetch word from study sessions state if active
        let wordToPlay = null;
        
        if (typeof session !== 'undefined' && session) {
            if (session.currentWord && session.currentWord.word) {
                wordToPlay = session.currentWord.word;
            } else if (session.queue) {
                const idx = typeof session.currentIndex !== 'undefined' ? session.currentIndex : session.index;
                if (session.queue[idx] && session.queue[idx].word) {
                    wordToPlay = session.queue[idx].word;
                }
            }
        }
        
        // Fallback: look for data-word on visible study card element
        if (!wordToPlay) {
            const elements = document.querySelectorAll('[data-word]');
            for (const el of elements) {
                if (el.offsetWidth > 0 && el.offsetHeight > 0) {
                    const w = el.getAttribute('data-word');
                    if (w && w !== 'Loading...') {
                        wordToPlay = w;
                        break;
                    }
                }
            }
        }
        
        if (wordToPlay) {
            e.preventDefault();
            playWord(wordToPlay, 'normal');
        }
    }
});
